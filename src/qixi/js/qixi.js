    var bindScroll = function( container ){

        var checkOffset = 50;
        var $win = $(window);
        var winHeight = $win.height();
        //var initScrollTop = $win.scrollTop();
        var elems = $(container).find('[data-anim]');
        var elemObj = [];

        elems.each(function(){
            elemObj.push({
                $elem: $(this),
                anim: this.getAttribute('data-anim'),
                scrollTop: $(this).offset().top,
                isAnimated: false
            });
        });

        $win.on('scroll', function(){
                var scrollTop = $win.scrollTop();
                var docHeight = document.documentElement.clientHeight;

                $.each(elemObj, function(i, obj){
                    if( !obj.isAnimated ) {
                        if( obj.$elem[0].getAttribute('data-ignore') ){
                            obj.isAnimated = true;
                            return;
                        }
                        if (scrollTop + docHeight - checkOffset > obj.scrollTop) {
                            setAnimate(obj.$elem[0], obj.scrollTop < winHeight - checkOffset);
                            obj.isAnimated = true;
                        }
                    }
                });
            })
            .trigger('scroll');
    };

    var FollowFlyBird = function(){
        this._canFollow = true;
        this.coords = [];
        this._isMcRunning = false;
        this._isFlyMcRunning = false;
        this.create();
        this.caculateCoords();
        this._initEvents();
    };

    FollowFlyBird.prototype = {
        create: function(){
            this.el = document.createElement('div');
            this.el.style.position = "absolute";
            this.el.style.position.zIndex = 11;
            this.birdEl = document.createElement('div');
            this.el.appendChild(this.birdEl);
            document.body.appendChild(this.el);

            this.shootEl = document.createElement('div');
            this.shootEl.style.position = "absolute";
            this.shootEl.style.display = "none";
            this.shootEl.style.cursor = 'default';
            document.body.appendChild(this.shootEl);

            //this.birdMc = setBirdMC(this.birdEl);
        },
        setPos: function(x, y){
            this.el.style.display = "block";
            this.el.style.left = (x||0) + "px";
            this.el.style.top = (y||0) + "px";
        },
        setShootPos: function(x, y){
            this.shootEl.style.display = "block";
            this.shootEl.style.left = x + "px";
            this.shootEl.style.top = y + "px";
        },
        setFollow: function( bool ){
            this._canFollow = bool;
        },
        caculateCoords: function(){
            var self = this;
            $("#evt_container .price-wrap").each(function(){
                var offset = $(this).offset();
                self.coords.push({
                    left: offset.left,
                    top: offset.top,
                    width: this.offsetWidth,
                    height: this.offsetHeight,
                    target: $(this)
                });
            });
        },
        setFly: function(){
            var self = this;
            self.setFollow(true);
            self.shootMc && (self.shootMc.stop().el.style.display = "none");
            self.birdMc = setBirdMC(self.birdEl);
            self._isFlyMcRunning = true;
        },
        setShoot: function(target){
            var self = this;
            var offset = target.offset();
            self.setFollow(false);
            self.birdMc.stop();
            self.el.style.display = "none";
            self.shootMc = setBirdShootMC(self.shootEl);
            self.setShootPos(offset.left + target.get(0).offsetWidth*0.4, offset.top);
            self._isMcRunning = true;
        },
        _fit: function(x, y){
            for(var i=0; i<this.coords.length; i++){
                var coord = this.coords[i];
                if( (x>coord.left&&x<(coord.left + coord.width)) && (y>coord.top&&y<(coord.top + coord.height)) ){
                    return coord.target;
                }
            }

            return false;
        },
        _initEvents: function(){
            var self = this;
            var $doc = $(document);
            $doc.on("mousemove", function(e){
                var target = self._fit(e.pageX, e.pageY);
                if (target) {
                    if( !self._isMcRunning ) {
                        self.setShoot(target);
                    }
                    self._isFlyMcRunning = false;
                } else {
                    self._isMcRunning = false;
                    if( !self._isFlyMcRunning ) {
                        self.setFly();
                    }
                }
                if( self._canFollow ) {
                    self.setPos(e.pageX - 150, e.pageY + 2);
                }
            }).trigger("mousemove");
        }
    };

